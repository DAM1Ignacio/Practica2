/**
 * Programa que inicializa 3 variables y las muestra por pantalla
 * @author ignacio
 * 18.10.11
 */
public class tresVar {
	public static void main (String[] Args) {
		// Declaracion
		int var0;
		int var1;
		int var2;
		// Asignacion de valor
		var0 = 1;
		var1 = 2;
		var2 = 3;
		// Muestra el valor de las variables por pantalla
		System.out.println(var0+" "+var1+" "+var2);
	}
}
